
WEB FRAMWORKD

 A framework simply abstracts the lower level code allowing you to focus on the rquirements than the code itself
For example, angular, vue and React are all framework that help you build user intercaces without having to rely on the lower level DOM API in javascript.

There are frameworks to build web or mobile applications without having to rely on the http module in node js

e.g express , nest , hapi. koa, sails


They build on top of the HTTP module , making it easier for you to implement all the features you have seen















































































































